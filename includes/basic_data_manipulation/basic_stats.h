#ifndef BASIC_STATS_H
#define BASIC_STATS_H

/**
 * @brief 
 *  Finds the larger of two inputted integers.
 * 
 * @param num1  integer 1
 * @param num2  integer 2
 * 
 * @return Larger integer.
 */
int max_int(const int num1, const int num2);

/**
 * @brief 
 *  Finds the smaller of two inputted integers.
 * 
 * @param num1  integer 1
 * @param num2  integer 2
 * 
 * @return Smaller integer.
 */
int min_int(const int num1, const int num2);

/**
 * @brief 
 *  Finds the larger of two inputted floats.
 * 
 * @param num1  float 1
 * @param num2  float 2
 * 
 * @return Larger float.
 */
float max_float(const float num1, const float num2);

/**
 * @brief 
 *  Finds the smaller of two inputted floats.
 * 
 * @param num1  float 1
 * @param num2  float 2
 * 
 * @return Smaller float.
 */
float min_float(const float num1, const float num2);

/**
 * @brief 
 *  Finds the larger of two inputted doubles.
 * 
 * @param num1  double 1
 * @param num2  double 2
 * 
 * @return Larger double.
 */
double max_double(const double num1, const double num2);

/**
 * @brief 
 *  Finds the smaller of two inputted doubles.
 * 
 * @param num1  double 1
 * @param num2  double 2
 * 
 * @return Smaller double.
 */
double min_double(const double num1, const double num2);
#endif